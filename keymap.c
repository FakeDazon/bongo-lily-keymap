//
// Keyboard initialization
//
void keyboard_post_init_user(void) {
    // Wait for keyboard startup
    wait_ms(100);
}


//
// Rotary encoder actuated
//
void encoder_update_user(uint8_t index, bool clockwise) {
    // Right rotary encoder
    if (index == 0) {
        // Arrow keys
        if (layer_state == 0) {
            if (clockwise) tap_code(KC_RIGHT);
            else tap_code(KC_LEFT);
        } else {
            if (clockwise) tap_code(KC_DOWN);
            else tap_code(KC_UP);
        }
    }

    // If LEDs have been turned off, restore previous brightness
    if (led_on == false || old_underglow_level == -1) {



        led_on = true;
    }

    idle_timer = timer_read();
    halfmin_counter = 0;
}

//
// Key processing
//
bool process_record_user(uint16_t keycode, keyrecord_t *record) {

    // Key pressed, restore previous backlight and underglow brightness if timeout has passed
    if (record -> event.pressed) {
        // If LEDs have been turned off, restore previous brightness
        if (led_on == false || old_underglow_level == -1) {


            led_on = true;
        }

        idle_timer = timer_read();
        halfmin_counter = 0;
    }

    // Get GUI keys status
    switch (keycode) {
        // GUI keys disabled
        case GUI_OFF:
            if (record -> event.pressed) gui_on = false;
            break;
        // GUI keys enabled
        case GUI_ON:
            if (record -> event.pressed) gui_on = true;
            break;
    }
    
    return true;
}

//
// Scan matrix
//
void matrix_scan_user(void) {

    if (idle_timer == 0) idle_timer = timer_read();

    if (led_on && timer_elapsed(idle_timer) > 30000) {
        halfmin_counter++;
        idle_timer = timer_read();
    }

    // Timeout has passed
    if (led_on && halfmin_counter >= led_timeout * 2) {


        led_on = false;
        halfmin_counter = 0;
    }
}

